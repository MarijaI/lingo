class Lingo

  require_optional 'nokogiri'

  class Filter

    class XML < self

      TYPE = :XML

      def initialize(*args)
        super
        @obj = Nokogiri.send(self.class::TYPE, @io, nil, @encoding)
      end

      def each
        @obj.children.each { |n| yield n.content }
      end

    end

    class HTML < XML

      TYPE = :HTML

    end

  end

end
